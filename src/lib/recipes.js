// ─── Total weight of a recipe portion (sum of ingredient.grams, skipping seasonings)
export function recipePortionGrams(recipe) {
  const total = recipe.ingredients.reduce((sum, ing) => sum + (ing.grams || 0), 0)
  return recipe.servings > 1 ? Math.round(total / recipe.servings) : total
}
